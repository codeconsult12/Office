package android.cs453.pdtapplication;

public class Company {
    String code;
    String name;

    Company() {

    }

    public String getCode() {
        return this.code;
    }
    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return this.name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
